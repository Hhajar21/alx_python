class Square:
    """
    This class represents a square.

    Attributes:
        __size (int): The size of the square.

    Methods:
        __init__(self, size):
            Initializes a square object with a given size.
    """

    def __init__(self, size):
        """
        Initializes a square object with a given size.

        Args:
            size (int): The size of the square.

        Raises:
            None.
        """
        self.__size = size
